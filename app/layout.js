// Importing the "Inter" font from Google Fonts via Next.js built-in font optimization feature.
// The `Inter` font will be applied to all text in the application to maintain consistent typography.
import { Inter } from "next/font/google";

// Importing the application context provider, which supplies global state and shared logic
// (e.g., alerts, messages, theme preferences, etc.) across all components in the app.
import { AppProvider } from "./components/context/AppContext";

// Importing the application's custom Material UI theme wrapper.
// `AppTheme` manages light/dark mode and other theme-related configurations for consistent styling.
import AppTheme from "./shared-theme/AppTheme";

// -------------------- FONT INITIALIZATION --------------------

// Initializes the Inter font and restricts it to the "latin" character subset for optimized loading performance.
const inter = Inter({ subsets: ["latin"] });

