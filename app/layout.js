/**
 * RootLayout — The foundational layout component that wraps every page in the Next.js application.
 *
 * Responsibilities:
 * - Defines the HTML structure (`<html>` and `<body>` tags) for all routes.
 * - Applies global font and theme settings.
 * - Provides application-wide state management through React Context.
 *
 * @param {object} props
 * @param {React.ReactNode} props.children - The nested page or component rendered inside the layout.
 * @returns {JSX.Element} The complete root HTML structure with global providers applied.
 */
export default function RootLayout({ children }) {
  return (
    // Defines the HTML language attribute for accessibility and SEO.
    <html lang="en">
      {/* The <body> element applies the global Inter font style */}
      <body className={inter.className}>
        {/* 
          AppTheme: Wraps the application with a Material UI ThemeProvider.
          - Ensures consistent color palette and component styling.
          - Handles dynamic theme changes (e.g., light/dark modes).
        */}
        <AppTheme>
          {/* 
            AppProvider: Wraps all children components with a React Context provider.
            - Supplies global state and shared methods to all nested components.
            - Reduces prop drilling and improves state consistency across the app.
          */}
          <AppProvider> {children}</AppProvider>
        </AppTheme>
      </body>
    </html>
  );
}
